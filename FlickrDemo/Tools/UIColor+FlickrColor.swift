import UIKit

extension UIColor {
    //Static colors
    public class func flickrButtonBlueColor() -> UIColor {
        return UIColor(red: 0.0/255.0, green: 94.0/255.0, blue: 255.0/255.0, alpha: 1.0)
    }
    
    public class func flickrButtonGrayColor() -> UIColor {
        return UIColor(red: 174.0/255.0, green: 175.0/255.0, blue: 176.0/255.0, alpha: 1.0)
    }
}
